#include<stdio.h>
#include<math.h>

int main()
{
    const float pi = 3.1415926;
    printf("2.0 = %f\n", 2.0);
    printf("pi = %f\n", pi);
    printf("cos(pi) = %f\n", cos(pi));
    printf("sin(pi) = %f\n", sin(pi));
    printf("sqrt(2.0) = %f\n", sqrt(2.0));
    printf("sqrt(2.0) / 2 = %f\n", sqrt(2.0) / 2);
}
